// Flat list of every chapter (parents + children) for quick slug lookup
export const allChapters = chapters.flatMap((c) => [
  c,
  ...(c.children || []).map((child) => ({ ...child, parentSlug: c.slug })),
]);

export function findChapter(slug) {
  return allChapters.find((c) => c.slug === slug);
}

export function findParent(slug) {
  const child = allChapters.find((c) => c.slug === slug);
  if (!child?.parentSlug) return null;
  return chapters.find((c) => c.slug === child.parentSlug);
}

// Translate a `globePins` id (used on the 3D globe) to the matching
// top-level chapter slug. Some globe pins (saudi, iran) sit under the
// shared "Arabia" federation, so they share that parent.
const GLOBE_PIN_TO_CHAPTER_SLUG = {
  usa: 'usa',
  canada: 'canada',
  mexico: 'mexico',
  uk: 'uk',
  europe: 'europe',
  turkey: 'turkiye',
  israel: 'israel',
  saudi: 'arabia',
  iran: 'arabia',
  africa: 'africa',
  china: 'china',
  australasia: 'australasia',
};

export function chapterForGlobePin(pinId) {
  const slug = GLOBE_PIN_TO_CHAPTER_SLUG[pinId];
  if (!slug) return null;
  return chapters.find((c) => c.slug === slug) || null;
}
